import { generateSearchTokens } from '@/lib/utils/format';

const now = new Date().toISOString();

function makeCollege(
  partial: Omit<College, 'searchTokens' | 'createdAt' | 'updatedAt'>,
): College {
  return {
    ...partial,
    searchTokens: generateSearchTokens(
      partial.name,
      partial.shortName,
      partial.location.city,
      partial.location.state,
      partial.type,
      partial.category,
    ),
    createdAt: now,
    updatedAt: now,
  };
}

